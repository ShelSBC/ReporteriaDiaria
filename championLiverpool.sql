select 
B1.DMACCT, 
B1.DMQUE, 
B1.DMAGENCY, 
B1.U6BLOCKCODE1, 
B1.U6BLOCKCODE2, 
B1.DMLABEL1, 
B1.U6ORG, 
B1.DMCURBAL, 
B1.CD_INI, 
B2.U6CYCDUE CD_FIN,
CASE WHEN (B2.U6BLOCKCODE1 = 'K' AND B1.CD_INI <B2.U6CYCDUE) THEN 'K'
WHEN (B2.U6CHGOFFSTAT=5 AND B2.U6CYCDUE=9 AND B1.CD_INI=9) THEN 'K'  else to_char(B2.U6CYCDUE) end as CD_FIN_K,
B2.U6BLOCKCODE1, 
B2.DMCURBAL, 
B3.PAGOS,
B2.U6CHGOFFSTAT

from 

(select DMACCT, DMQUE, DMAGENCY, U6BLOCKCODE1, U6BLOCKCODE2, DMLABEL1, U6ORG, DMCURBAL, U6CYCDUE as CD_INI
from RCVRY.DELQUDA_BEGIN
where trunc(BEGINDT) = TO_DATE('01-07-2025','DD-MM-YYYY')
and DMAGENCY IN ('DA006EQC','DA006LRS','DA006MSE','DA006PLA','DA006RCC','DA007BJV','DA007MAM','DA007OCA','DA007YZA',
                 'DA008GAA','DA008AYE','DA008PLA','DA008MSE','DA009ALD','DA009EQC','DA009OCA','DA009YZA')) B1

left outer join

(select DMACCT, U6CYCDUE, DMCURBAL, DMSTATUS,U6CHGOFFSTAT, U6BLOCKCODE1
from RCVRY.DELQMST, RCVRY.UDA6
where DMACCTG = '6'
and DMACCT in (
select DMACCT
from RCVRY.DELQUDA_BEGIN
where trunc(BEGINDT) = TO_DATE('01-07-2025','DD-MM-YYYY')
and DMAGENCY IN ('DA006EQC','DA006LRS','DA006MSE','DA006PLA','DA006RCC','DA007BJV','DA007DAD','DA007MAM','DA007OCA','DA007YZA',
                 'DA008MYM','DA008AYE','DA008PLA','DA008RCC','DA009ALD','DA009EQC','DA009OCA','DA009YZA')
)
and DMACCTG = U6ACCTG
and DMACCT = U6ACCT) B2

on B1.DMACCT = B2.DMACCT

left outer join

(select PMACCT, PMAGENCY,sum(PMTAMT) as PAGOS
from RCVRY.PMTFIL
where PMACCTG = '6'
and (trunc(PMEDATE) >= TO_DATE('01-07-2025','DD-MM-YYYY') and trunc(PMEDATE) <= TO_DATE('31-07-2025','DD-MM-YYYY'))
and PMACCT in (
select DMACCT
from RCVRY.DELQUDA_BEGIN
where trunc(BEGINDT) = TO_DATE('01-06-2025','DD-MM-YYYY')

and DMAGENCY IN ('DA006EQC','DA006LRS','DA006MSE','DA006PLA','DA006RCC','DA007BJV','DA007DAD','DA007MAM','DA007OCA','DA007YZA',
                 'DA008MYM','DA008AYE','DA008PLA','DA008RCC','DA009ALD','DA009EQC','DA009OCA','DA009YZA')
)
group by PMACCT, PMAGENCY) B3
on B1.DMACCT = B3.PMACCT AND B1.DMAGENCY=B3.PMAGENCY